    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int fila = jTable1.getSelectedRow();
        if (fila != -1) {
            if (!modoEdicion) {
                jButtonEditar.setText("Actualizar");
                TableModel model = jTable1.getModel();
                jComboBox1.setSelectedItem((String) model.getValueAt(fila, 0));
                txtNombre.setText((String) model.getValueAt(fila, 1));
                txtPrecio.setText(String.valueOf(model.getValueAt(fila, 2)));
                modoEdicion = true;
            } else {
                Categoria selectedCategoria = (Categoria) jComboBox1.getSelectedItem();
                String nombreCategoria = selectedCategoria.name();
                String nombre = txtNombre.getText();
                double precio = Double.parseDouble(txtPrecio.getText());
                TableModel model = jTable1.getModel();
                
                model.setValueAt(nombreCategoria, fila, 0);
                model.setValueAt(nombre, fila, 1);
                model.setValueAt(precio, fila, 2);
                jButtonEditar.setText("Editar");
                modoEdicion = false;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Producto");
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        int fila = jTable1.getSelectedRow();
        if (fila!=-1) {
            modelo.removeRow(fila);
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Producto");
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    
