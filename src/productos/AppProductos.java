    private void buttonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAgregarActionPerformed
        String nombre = "";
        Double precio = 0.0;
        try {
            if (!txtNombre.getText().equals("") || !txtNombre.getText().isEmpty()
                    && !txtPrecio.getText().equals("") || !txtPrecio.getText().isEmpty()) {
                nombre = txtNombre.getText();
                precio = Double.parseDouble(txtPrecio.getText());
                Categoria selectedCategoria = (Categoria) jComboBox1.getSelectedItem();
                String nombreCategoria = selectedCategoria.name();
                Producto p = new Producto(nombreCategoria, nombre, precio);
                cargarTabla(p);
                JOptionPane.showMessageDialog(this, "Se ha cargado el producto");
            } else {
                JOptionPane.showMessageDialog(this, "Complete los campos");
            }
        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(this, "Verifique los datos ingresados");
        }
        txtNombre.setText("");
        txtPrecio.setText("");
        jComboBox1.setSelectedIndex(-1);
    }//GEN-LAST:event_buttonAgregarActionPerformed

    private void cargarCabecera(){
        modelo.addColumn("Categoria");
        modelo.addColumn("Nombre");
        modelo.addColumn("Precio");
        jTable1.setModel(modelo);       
    }
    
    private void cargarTabla(Producto producto) {
       modelo.addRow(new Object[]{producto.getCategoria(), producto.getNombre(),producto.getPrecio()});
    }
    
